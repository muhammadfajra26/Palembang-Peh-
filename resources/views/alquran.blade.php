        <h3>AL-QURAN AL-AKBAR</h3>
        <p>Al Quran Al-Akbar atau yang juga sering disebut Al Quran Raksasa yang berada di kota Palembang beralamat di Pondok Pesantren Al Ihsaniyah Gandus Palembang. Terdapat 30 juz ayat suci Al-Quran yang berhasil dipahat/diukir ala khas Palembang dalam lembar kayu dan menghabiskan kurang lebih 40 meter kubik kayu tembesu dengan biaya tidak kurang Rp 2 miliar, dimana masing-masing lembar ukuran halamannya 177 x 140 x 2,5 sentimeter dan tebal keseluruhannya termasuk sampul mencapai 9 meter.

            Al-Quran yang terdiri dari 630 halaman ini juga dilengkapi dengan tajwid serta doa khataman bagi pemula. Setiap lembar terpahat ayat suci Al-Quran pada warna dasar kayu coklat dengan huruf arab timbul warna kuning dengan ukiran motif kembang di bagian tepi ornamen khas Palembang yang sangat indah di pandang dan enak dibaca. Proses pembuatannya sendiri memakan waktu relatif lama, sekitar tujuh tahunan.
            
            Al Quran Raksasa ini terbesar dan pertama di dunia dalam bentuk Al Quran 30 juz yang di buat pada media dari kayu jenis tembesu. Al-Quran terbesar ini sebelum resmi dipublikasikan, sengaja di pajang seluruh ayat-ayat suci di dalam ruang pamer Masjid Agung Palembang selama tiga tahun untuk mendapat koreksi dari seluruh umat.</p>
