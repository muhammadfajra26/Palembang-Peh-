        <h3>PULAU KEMARO</h3>
        <p>Pulau Kemaro terletak di kawasan Sungai Musi dan bisa ditempuh dalam waktu 20 menit dari Dermaga Benteng Kuto Besak. Pulau ini tidak terlalu besar karena luasnya kurang lebih hanya 30 hektar dan dihuni ratusan orang saja. Namun, Pulau Kemaro tidak pernah sepi karena selalu dipadati oleh wisatawan.Salah satu daya tarik Pulau Kemaro terletak pada bangunan pagoda di bagian tengah pulau. Arsitektur pagoda tersebut sangat mirip dengan pagoda ala negeri tirai bambu. Sisi-sisi dinding pagoda sembilan lantai ini menggambarkan legenda Pulau Kemaro. Bagian atas pagoda terdiri dari tempat ibadah umat Buddha yang sering digunakan masyarakat etnis Tionghoa sehingga tak mengherankan bila pagoda ini ramai dikunjungi menjelang hari raya Imlek atau hari raya Buddha lainnya. Bahkan, beberapa ruangan di dalam pagoda juga sering digunakan para peserta festival Imlek untuk menginap.
        Tepat di samping pagoda, ada klenteng Hok Tjing Bio yang lebih populer disebut klenteng Kwan Im. Di depan klenteng yang dibangun tahun 1962 ini terdapat makam Tan Bun An, Siti Fatimah, dan pengawalnya yang dipercaya sebagai asal usul terbentuknya Pulau Kemaro.</p>
