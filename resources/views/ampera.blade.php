        <h3>Jembatan Ampera</h3>
        <p>Jembatan Ampera (Huruf Jawi : جمبتن أمڤيرا) (Amanat penderitaan rakyat) adalah sebuah jembatan di Kota Palembang, Provinsi Sumatra Selatan, Indonesia. Jembatan Ampera, yang telah menjadi semacam lambang kota, terletak di tengah-tengah Kota Palembang, menghubungkan daerah Seberang Ulu dan Seberang Ilir yang dipisahkan oleh Sungai Musi. Jembatan Ampera merupakan ikon kota Palembang yang paling terkenal.Pada awalnya, bagian tengah dan bagian belakang dan bagian depan badan jembatan ini bisa diangkat ke atas agar tiang kapal yang lewat dibawahnya tidak tersangkut badan jembatan. Bagian tengah jembatan dapat diangkat dengan peralatan mekanis, dua bandul pemberat masing-masing sekitar 500 ton di dua menaranya. Kecepatan pengangkatannya sekitar 10 meter per menit dengan total waktu yang diperlukan untuk mengangkat penuh jembatan selama 30 menit.
        Pada saat bagian tengah jembatan diangkat, kapal dengan ukuran lebar 60 meter dan dengan tinggi maksimum 44,50 meter, bisa lewat mengarungi Sungai Musi. Bila bagian tengah jembatan ini tidak diangkat, tinggi kapal maksimum yang bisa lewat di bawah Jembatan Ampera hanya sembilan meter dari permukaan air sungai</p>
