        <h3>BENTENG KUTO BESAK</h3>
        <p>Benteng ini mulai dibangun pada tahun 1780 dengan arsitek yang tidak diketahui dengan pasti dan pelaksanaan pengawasan pekerjaan dipercayakan pada seorang Tionghoa. Semen perekat bata menggunakan batu kapur yang ada di daerah pedalaman Sungai Ogan ditambah dengan putih telur. Waktu yang dipergunakan untuk membangun Kuto Besak ini kurang lebih 17 tahun. Keraton ini ditempati secara resmi pada hari Senin pada tanggal 21 Februari 1797.

            Berbeda dengan letak keraton lama yang berlokasi di daerah pedalaman, keraton baru berdiri di posisi yang sangat terbuka, strategis, dan sekaligus sangat indah. Posisinya menghadap ke Sungai Musi.
            
            Pada masa itu, Kota Palembang masih dikelilingi oleh anak-anak sungai yang membelah wilayah kota menjadi pulau-pulau. Kuto Besak pun seolah berdiri di atas pulau karena dibatasi oleh Sungai Sekanak di bagian barat, Sungai Tengkuruk di bagian timur, dan Sungai Kapuran di bagian utara.
            
            Benteng Kuto Besak saat ini ditempati oleh Komando Daerah Militer (Kodam) Sriwijaya.</p>
